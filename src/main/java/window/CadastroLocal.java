    @Override
    protected void limparDados(){
        cidadeField.setText("");
        cepField.setText("");
        bairroField.setText("");
        compleField.setText("");
        logradField.setText("");
        numField.setText("");
    }
    
    @Override
    protected void preencherDados(){
        cidadeField.setText(objectToBeEdited.getCidade());
        cepField.setText(objectToBeEdited.getCep());
        bairroField.setText(objectToBeEdited.getBairro());
        compleField.setText(objectToBeEdited.getComplemento());
        logradField.setText(objectToBeEdited.getLogradouro());
        numField.setText(String.valueOf(objectToBeEdited.getNumero()));
    }
    
    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed

        String cidade = cidadeField.getText();
        String cep = cepField.getText();
        String bairro = bairroField.getText();
        String complemento = compleField.getText();
        String logradouro = logradField.getText();
        int num = Integer.parseInt(numField.getText());
        
        if (objectToBeEdited == null) {
            GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().inserirLocal(cidade, logradouro, num, complemento, bairro, cep);
        } else {
            GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().alterarLocal(objectToBeEdited.getId(), cidade, logradouro, num, complemento, bairro, cep);
        }
        
        this.setVisible(false);
        
    }//GEN-LAST:event_salvarButtonActionPerformed
