    private void pesquisarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarButtonActionPerformed
        local_selecionado = (Local) GerenciadorInterGrafica.getInstance().abrirBuscaLocal(BuscaGenerica.Modo.SELECAO);
        if (local_selecionado != null){
            localField.setText(local_selecionado.toString());
        }
    }//GEN-LAST:event_pesquisarButtonActionPerformed

    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        String nome = nomeField.getText();
        Local local = local_selecionado;
        Instituicao.Tipo tipo = Instituicao.Tipo.values()[cmbTipo.getSelectedIndex()];
        
        if (objectToBeEdited == null) {
            GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().inserirInstituicao(nome, local, tipo);
        } else {
            GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().alterarInstituicao(objectToBeEdited.getId(), nome, local, tipo);
        }
        
        this.setVisible(false);
    }//GEN-LAST:event_salvarButtonActionPerformed

