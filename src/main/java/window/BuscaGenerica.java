    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        int linha = tableBusca.getSelectedRow();
        if(linha >= 0){
            objeto_selecionado = table_model.getObjectAt(linha);
            if (JOptionPane.showConfirmDialog(this, "Deseja excluir a linha selecionada?", "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().excluir(objeto_selecionado);
                botaoPesquisarActionPerformed(evt);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.", "", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_excluirButtonActionPerformed

    private void cmBoxOpcoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmBoxOpcoesActionPerformed
        int i = cmBoxOpcoes.getSelectedIndex();
        Class classe_field = table_model.getFieldClass(i);
        
        fieldPesquisa.setText("");
        
        if (classe_field.isEnum()) {
            fieldData.setVisible(false);
            cmBoxPesqEnum.removeAllItems();
            fieldPesquisa.setVisible(false);
            cmBoxPesqEnum.setVisible(true);
            Object[] enum_values = classe_field.getEnumConstants();
            for (Object enum_value : enum_values) {
                cmBoxPesqEnum.addItem(enum_value.toString());
            }
            tipo_pesquisa = TipoPesquisa.ENUM;
        } else if (table_model.haveFieldManyToOneAnnotation(i)){
            fieldPesquisa.setVisible(true);
            fieldPesquisa.setEditable(true);
            cmBoxPesqEnum.setVisible(false);
            fieldData.setVisible(false);
            foreign_key_class = classe_field;
            tipo_pesquisa = TipoPesquisa.FOREIGN_KEY;
            fieldPesquisa.setEditable(false);
        } else if (classe_field == Date.class){
            fieldData.setVisible(true);
            fieldPesquisa.setVisible(false);
            cmBoxPesqEnum.setVisible(false);
            tipo_pesquisa = TipoPesquisa.DATE;
        } else {
            fieldData.setVisible(false);
            fieldPesquisa.setVisible(true);
            fieldPesquisa.setEditable(true);
            cmBoxPesqEnum.setVisible(false);            
            if (classe_field == String.class){
                tipo_pesquisa = TipoPesquisa.STRING;
            } else if (classe_field == int.class) {
                tipo_pesquisa = TipoPesquisa.INT;
            }
        }
        

    }//GEN-LAST:event_cmBoxOpcoesActionPerformed

