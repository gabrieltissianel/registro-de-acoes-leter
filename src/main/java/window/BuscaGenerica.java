    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        List<Object> lista = new ArrayList<>();
       
        if (null != tipo_pesquisa) switch (tipo_pesquisa) {
            case ENUM:
                lista = GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().pesquisarPorInt
                        (classe, table_model.getPersistenceColumnName(cmBoxOpcoes.getSelectedIndex()), cmBoxPesqEnum.getSelectedIndex());
                break;
            case FOREIGN_KEY:
                foreign_key_object = GerenciadorInterGrafica.getInstance().abrirBusca(foreign_key_class);
                fieldPesquisa.setText(foreign_key_object.toString());
                
                int id = 0;
                try {
                    id = (int) foreign_key_class.getMethod("getId").invoke(foreign_key_object);
                } catch (NoSuchMethodException | SecurityException | IllegalAccessException | InvocationTargetException ex) {
                    Logger.getLogger(BuscaGenerica.class.getName()).log(Level.SEVERE, null, ex);
                } 
                
                lista = GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().pesquisarPorForeignKey
                        (classe, table_model.getPersistenceColumnName(cmBoxOpcoes.getSelectedIndex()), id);
                break;
            case STRING:
                lista = GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().pesquisarPorString
                        (classe, table_model.getPersistenceColumnName(cmBoxOpcoes.getSelectedIndex()), fieldPesquisa.getText());
                break;
            case INT:
                lista = GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().pesquisarPorInt
                        (classe, table_model.getPersistenceColumnName(cmBoxOpcoes.getSelectedIndex()), Integer.parseInt(fieldPesquisa.getText()));
                break;
            case DATE:
                try {
                    lista = GerenciadorInterGrafica.getInstance().getGerenciadorDeDominio().pesquisarPorData
                                (classe, table_model.getPersistenceColumnName(cmBoxOpcoes.getSelectedIndex()), FuncoesUteis.strToDate(fieldData.getText()));
                } catch (ParseException ex) {
                    JOptionPane.showMessageDialog(this, "Insira uma data v√°lida.");
                }
        }
        
        table_model.setList(lista);    
    }//GEN-LAST:event_botaoPesquisarActionPerformed

    private void selectOrEditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectOrEditButtonActionPerformed
        int linha = tableBusca.getSelectedRow();
        try{
            objeto_selecionado = table_model.getObjectAt(linha);
            if (modo == Modo.EDICAO) {
                GerenciadorInterGrafica.getInstance().abrirCad(objeto_selecionado, classe);
                botaoPesquisarActionPerformed(evt);
                objeto_selecionado = null;
            } else {
                this.setVisible(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para realizar a a√ß√£o.", "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_selectOrEditButtonActionPerformed

